package day06_TypeCasting;

public class TypeCasting {
    public static void main(String[] args) {

        byte b=5;
        short s=129;

        b=(byte)s;
        System.out.println(b);

        int i=10;
        double d=20;
        d=i;
        i= (int) d;

        System.out.println(i);












    }
}
